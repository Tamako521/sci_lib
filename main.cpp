// 引入Qt应用程序类
// 作用：提供Qt图形界面程序的核心运行环境（事件循环、窗口管理等）
#include <QApplication>

// 引入自定义主窗口类的头文件
// 作用：让main函数知道MainWindow是什么，才能创建窗口对象
#include "mainwindow.h"

// main函数：C++程序的**唯一入口**，程序从这里开始运行
// argc：命令行参数个数
// argv：命令行参数数组
int main(int argc, char *argv[])
{
    // 1. 创建Qt应用程序对象
    // 作用：初始化整个Qt图形界面环境，处理鼠标、键盘、窗口刷新等所有系统事件
    QApplication a(argc, argv);

    // 2. 创建自定义的主窗口对象
    // 作用：调用MainWindow构造函数，初始化界面、控件、加载XML数据、绑定按钮事件等
    MainWindow w;

    // 3. 显示主窗口
    // 作用：让创建好的窗口在屏幕上显示出来（默认创建后是隐藏的）
    w.show();

    // 4. 启动Qt的事件循环
    // 作用：让程序保持运行，等待用户操作（点击、输入、关闭等），直到关闭窗口才退出
    // exec()是一个阻塞函数，程序关闭后才会返回0
    return a.exec();
}

//main.cpp 就是整个程序的启动器，只做四件事：
//启动 Qt 环境
//创建主窗口
//显示窗口
//让程序一直运行，等待用户操作